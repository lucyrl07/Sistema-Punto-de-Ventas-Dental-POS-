    //Metodo para enviar datos seleccionados
    private void EnviarDatosAdministradorSeleccionado(int idTratamiento) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(""
                    + "select * from tb_administrador where idAdministrador= '" + idAdministrador + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellido.setText(rs.getString("apellido"));
                txt_usuario.setText(rs.getString("usuario"));
                txt_password.setText(rs.getString("password"));
                txt_telefono.setText(rs.getString("telefono"));

            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al seleccionar Administrador: " + e);

        }
    }
    
