//Metodo para mostrar todos los clientes registrados
    private void CargarTablaAdministrador() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select * from tb_administrador";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarAdministrador.jTable_administradores = new JTable(model);
            InterGestionarAdministrador.jScrollPane1.setViewportView(InterGestionarAdministrador.jTable_administradores);

            model.addColumn("N°"); //ID
            model.addColumn("nombre");
            model.addColumn("apellido");
            model.addColumn("usuario");
            model.addColumn("password");
            model.addColumn("telefono");
            model.addColumn("estado");

            while (rs.next()) {
                Object fila[] = new Object[7];
                for (int i = 0; i < 7; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla Administrador: " + e);
        }

        jTable_administradores.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_administradores.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idAdministrador = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosAdministradorSeleccionado(idAdministrador);

                }
            }
        });
    }

